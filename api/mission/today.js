/**
 * Hero Academy \u2014 Today\u2019s Mission generator.
 *
 * POST /api/mission/today
 * Body: {
 *   day_of_week:   number (0\u20136),
 *   day_name:      string,
 *   zone_progress: { [zoneId]: number 0\u2013100 },
 *   recent_zones:  string[]  // zones visited in last 3 days (most recent first)
 *   homework_due:  boolean
 *   homework_topic:string|null
 * }
 *
 * Returns: {
 *   warmup:  { zone_id, title, blurb, minutes },
 *   stretch: { zone_id, title, blurb, minutes },
 *   win:     { zone_id, title, blurb, minutes },
 *   total_minutes: number,
 *   unlock_hint:   string   // e.g. "Finish all 3 and unlock Aurora the Aviator!"
 * }
 *
 * Generated by Claude Haiku with strict JSON output. Falls back to a
 * deterministic mix if the model is unavailable.
 */

const HAIKU_MODEL = 'claude-haiku-4-5';

