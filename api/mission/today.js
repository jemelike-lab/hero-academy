function rewardCharacterForMission(m) {
  if (!m) return REWARD_FALLBACK;
  var stretchZone = m.stretch && m.stretch.zone_id;
  return REWARD_CHARACTER_FOR_ZONE[stretchZone] || REWARD_FALLBACK;
}

